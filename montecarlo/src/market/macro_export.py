class MacroEmailSender:
    """Send Global Macro Snapshot by email via SMTP."""

    def __init__(self, smtp_server: str = "smtp.gmail.com", smtp_port: int = 587,
                 smtp_user: str = "", smtp_password: str = ""):
        self.server   = smtp_server
        self.port     = smtp_port
        self.user     = smtp_user
        self.password = smtp_password

    def build_html_body(self, data: dict) -> str:
        """Build responsive HTML email body."""
        ts  = MacroExcelExporter._utc_timestamp(data.get('timestamp')).strftime('%B %d, %Y — %H:%M UTC')
        idx = data.get('indices', pd.DataFrame())
        fx  = data.get('fx', pd.DataFrame())
        comm= data.get('commodities', pd.DataFrame())
        vr  = data.get('vol_rates', {})
        cal = data.get('calendar', pd.DataFrame())

        def row_html(label, val, chg, fmt=".2f"):
            css = "#00C853" if (chg or 0) >= 0 else "#FF1744"
            sign = "+" if (chg or 0) >= 0 else ""
            return (f'<tr><td style="padding:2px 8px;color:#8B949E;font-size:12px">{label}</td>'
                    f'<td style="padding:2px 8px;color:#E6EDF3;font-size:12px;font-weight:600">'
                    f'{val}</td>'
                    f'<td style="padding:2px 8px;color:{css};font-size:12px">'
                    f'{sign}{chg:{fmt}}%</td></tr>')

        def section(title, rows_html):
            return (f'<div style="margin:12px 0">'
                    f'<div style="color:#00D9A6;font-size:11px;font-weight:700;'
                    f'letter-spacing:.08em;text-transform:uppercase;'
                    f'border-bottom:1px solid #1F2937;padding-bottom:4px;margin-bottom:6px">'
                    f'{title}</div>'
                    f'<table style="width:100%;border-collapse:collapse">{rows_html}</table>'
                    f'</div>')

        # Build sections
        idx_rows = ""
        if not idx.empty:
            for _, r in idx.head(8).iterrows():
                idx_rows += row_html(f'{r.get("flag","")} {r.get("name","")}',
                                     f'{r.get("last",0):,.2f}', r.get('change_pct', 0))

        fx_rows = ""
        if not fx.empty:
            for _, r in fx[fx['group']=='G10'].head(5).iterrows():
                fx_rows += row_html(f'{r.get("flag","")} {r.get("name","")}',
                                    f'{r.get("last",0):.4f}', r.get('change_pct', 0))

        comm_rows = ""
        if not comm.empty:
            for _, r in comm.head(5).iterrows():
                comm_rows += row_html(f'{r.get("flag","")} {r.get("name","")}',
                                      f'{r.get("last",0):,.2f}', r.get('change_pct', 0))

        rates_rows = ""
        for tkr, label in [('^VIX','VIX'), ('^TNX','US 10Y'), ('^TYX','US 30Y'), ('^IRX','US 13W')]:
            d = vr.get(tkr, {}) if vr else {}
            if d.get('last'):
                rates_rows += row_html(label, f'{d["last"]:.3f}%', d.get('chg_pct', 0))

        # Curve
        slope = vr.get('curve_slope') if vr else None
        curve_txt = ""
        if slope is not None:
            inv = "WARNING INVERTED" if slope < 0 else "Normal"
            curve_txt = f'<tr><td style="padding:2px 8px;color:#8B949E;font-size:12px">Curve 10Y–13W</td><td style="padding:2px 8px;color:#E6EDF3;font-size:12px">{slope:+.3f}%</td><td style="font-size:12px;padding:2px 8px">{inv}</td></tr>'
        rates_rows += curve_txt

        cal_rows = ""
        if not cal.empty:
            for _, r in cal[cal['importance'] == 'High'].head(4).iterrows():
                imp_color = "#FF1744"
                cal_rows += (f'<tr><td style="padding:2px 8px;color:#8B949E;font-size:12px">'
                             f'{r.get("flag","")} {r.get("date","")} {r.get("time","")}</td>'
                             f'<td style="padding:2px 8px;color:#E6EDF3;font-size:12px" colspan="2">'
                             f'{r.get("event","")}</td></tr>')

        body = f"""
<!DOCTYPE html>
<html>
<body style="background:#0A0A0F;color:#E6EDF3;font-family:Inter,Arial,sans-serif;margin:0;padding:0">
<table width="600" style="margin:0 auto;background:#111827;border:1px solid #1F2937;border-radius:8px">
  <tr><td style="padding:20px;background:#0D0D20;border-radius:8px 8px 0 0">
    <div style="font-size:20px;font-weight:700;color:#00D9A6"> RAVINALA</div>
    <div style="font-size:13px;color:#8B949E">Global Macro Snapshot</div>
    <div style="font-size:11px;color:#5B7FA6;margin-top:4px">{ts}</div>
  </td></tr>
  <tr><td style="padding:0 20px 20px 20px">
    {section("Equity Indices", idx_rows) if idx_rows else ""}
    {section("Rates & Volatility", rates_rows) if rates_rows else ""}
    {section("FX — G10", fx_rows) if fx_rows else ""}
    {section("Commodities", comm_rows) if comm_rows else ""}
    {section("High-Impact Events", cal_rows) if cal_rows else ""}
    <div style="margin-top:16px;padding-top:12px;border-top:1px solid #1F2937;
                color:#5B7FA6;font-size:10px">
       Full Excel report attached · Generated by Ravinala v2.0 · ravinala.io
    </div>
  </td></tr>
</table>
</body>
</html>
"""
        return body

    def send_snapshot(self, recipient_email: str, data: dict,
                      attach_excel: bool = True,
                      subject: Optional[str] = None) -> bool:
        """Send snapshot email. Returns True on success."""
        if not self.user or not self.password:
            return False
        ts = MacroExcelExporter._utc_timestamp(data.get('timestamp'))
        if subject is None:
            subject = f"Ravinala Macro Snapshot — {ts.strftime('%B %d, %Y %H:%M')} UTC"

        msg = MIMEMultipart('alternative')
        msg['Subject'] = subject
        msg['From']    = self.user
        msg['To']      = recipient_email

        html_body = self.build_html_body(data)
        msg.attach(MIMEText(html_body, 'html'))

        if attach_excel:
            try:
                exporter = MacroExcelExporter()
                xl_bytes = exporter.export_bytes(data)
                part = MIMEBase('application',
                                'vnd.openxmlformats-officedocument.spreadsheetml.sheet')
                part.set_payload(xl_bytes)
                encoders.encode_base64(part)
                fname = f"ravinala_macro_{ts.strftime('%Y%m%d_%H%M')}.xlsx"
                part.add_header('Content-Disposition', 'attachment', filename=fname)
                msg.attach(part)
            except Exception:
                pass  # Send without attachment if Excel fails

        try:
            with smtplib.SMTP(self.server, self.port, timeout=15) as server:
                server.ehlo()
                server.starttls()
                server.login(self.user, self.password)
                server.sendmail(self.user, recipient_email, msg.as_string())
            return True
        except Exception:
            return False
