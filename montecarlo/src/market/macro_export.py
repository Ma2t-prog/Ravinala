class MacroExcelExporter:
    """Generate a Bloomberg-style Excel snapshot of global macro data."""

    # Colour palette
    _HDR_FILL  = "1B2838"
    _ALT_FILL  = "1A2035"
    _BG_FILL   = "0E1117"
    _POS_FONT  = "00C853"
    _NEG_FONT  = "FF1744"
    _HDR_FONT  = "FFFFFF"
    _DATA_FONT = "E6EDF3"
    _ACC_FONT  = "00D9A6"

    def _wb_styles(self):
        """Return style helpers dict."""
        return {
            'hdr_font':  Font(bold=True, color=self._HDR_FONT, size=10, name='Calibri'),
            'data_font': Font(color=self._DATA_FONT, size=9, name='Calibri'),
            'pos_font':  Font(color=self._POS_FONT, size=9, name='Calibri', bold=True),
            'neg_font':  Font(color=self._NEG_FONT, size=9, name='Calibri', bold=True),
            'title_font':Font(bold=True, color=self._ACC_FONT, size=12, name='Calibri'),
            'hdr_fill':  PatternFill("solid", fgColor=self._HDR_FILL),
            'alt_fill':  PatternFill("solid", fgColor=self._ALT_FILL),
            'bg_fill':   PatternFill("solid", fgColor=self._BG_FILL),
            'thin_border': Border(
                bottom=Side(style='thin', color='30363D'),
            ),
            'center': Alignment(horizontal='center', vertical='center'),
            'right':  Alignment(horizontal='right',  vertical='center'),
            'left':   Alignment(horizontal='left',   vertical='center'),
        }

    def _write_header(self, ws, cols: list, s: dict):
        """Write styled header row."""
        for ci, col_name in enumerate(cols, 1):
            cell = ws.cell(row=1, column=ci, value=col_name)
            cell.font = s['hdr_font']
            cell.fill = s['hdr_fill']
            cell.alignment = s['center']

    def _write_data_row(self, ws, row_idx: int, values: list, s: dict,
                         chg_cols: list = None):
        fill = s['alt_fill'] if row_idx % 2 == 0 else s['bg_fill']
        for ci, val in enumerate(values, 1):
            cell = ws.cell(row=row_idx + 1, column=ci, value=val)
            cell.fill = fill
            cell.border = s['thin_border']
            if chg_cols and ci in chg_cols and isinstance(val, (int, float)):
                cell.font = s['pos_font'] if val >= 0 else s['neg_font']
            else:
                cell.font = s['data_font']
                cell.alignment = s['right'] if isinstance(val, (int, float)) else s['left']

    def _autofit(self, ws, min_w: int = 8, max_w: int = 40):
        for col in ws.columns:
            mx = max((len(str(c.value)) for c in col if c.value), default=min_w)
            ws.column_dimensions[get_column_letter(col[0].column)].width = min(max(mx + 2, min_w), max_w)

    def _add_title(self, ws, title: str, ts: str, s: dict):
        ws.sheet_view.showGridLines = False
        ws.cell(1, 1).value = title
        ws.cell(1, 1).font = s['title_font']
        ws.cell(2, 1).value = f"Generated by Ravinala v2.0 — {ts}"
        ws.cell(2, 1).font = Font(color="5B7FA6", size=8, italic=True)
        return 4  # data starts at row 4

    def _sheet_df(self, wb, title: str, df: pd.DataFrame, ts: str,
                   chg_col_names: list = None):
        """Add a DataFrame sheet with full styling."""
        if not _XL_OK or df is None or df.empty:
            return
        s   = self._wb_styles()
        ws  = wb.create_sheet(title=title[:31])
        off = self._add_title(ws, title, ts, s)
        cols = [c for c in df.columns if c not in ('sparkline',)]
        chg_indices = [i+1 for i, c in enumerate(cols) if c in (chg_col_names or [])]
        ws.freeze_panes = ws.cell(off + 1, 1)
        for ci, col in enumerate(cols, 1):
            cell = ws.cell(row=off, column=ci, value=col)
            cell.font = s['hdr_font']
            cell.fill = s['hdr_fill']
            cell.alignment = s['center']
        for ri, row in enumerate(df[cols].itertuples(index=False), 1):
            vals = list(row)
            fill = s['alt_fill'] if ri % 2 == 0 else s['bg_fill']
            for ci, val in enumerate(vals, 1):
                cell = ws.cell(row=off + ri, column=ci, value=val)
                cell.fill = fill
                cell.border = s['thin_border']
                if ci in chg_indices and isinstance(val, (int, float)):
                    cell.font = s['pos_font'] if val >= 0 else s['neg_font']
                else:
                    cell.font = s['data_font']
                    cell.alignment = s['right'] if isinstance(val, (int, float)) else s['left']
        self._autofit(ws)

    @staticmethod
    def _utc_timestamp(value: object | None = None) -> datetime:
        """Normalize optional timestamp inputs to timezone-aware UTC datetimes."""
        if value is None:
            return datetime.now(timezone.utc)
        if isinstance(value, datetime):
            return value if value.tzinfo is not None else value.replace(tzinfo=timezone.utc)
        return datetime.now(timezone.utc)

    def export_bytes(self, data: dict) -> bytes:
        """Generate Excel workbook and return as bytes."""
        if not _XL_OK:
            raise ImportError("openpyxl is required for Excel export.")
        from openpyxl import Workbook
        wb = Workbook()
        wb.remove(wb.active)  # remove default sheet
        ts = self._utc_timestamp(data.get('timestamp')).strftime('%Y-%m-%d %H:%M UTC')
        s  = self._wb_styles()

        # ── Dashboard summary sheet ──────────────────────────────
        ws_dash = wb.create_sheet("Dashboard")
        ws_dash.sheet_view.showGridLines = False
        ws_dash.cell(1, 1).value = "RAVINALA — Global Macro Snapshot"
        ws_dash.cell(1, 1).font = Font(bold=True, color="00D9A6", size=14)
        ws_dash.cell(2, 1).value = ts
        ws_dash.cell(2, 1).font = Font(color="5B7FA6", size=9, italic=True)

        row = 4
        sections = [
            ("Equity Indices", data.get('indices', pd.DataFrame()),
             ['name', 'flag', 'group', 'last', 'change_pct', 'ytd_pct'],
             ['change_pct', 'ytd_pct']),
            ("FX Rates", data.get('fx', pd.DataFrame()),
             ['name', 'flag', 'group', 'last', 'change_pct'],
             ['change_pct']),
            ("Commodities", data.get('commodities', pd.DataFrame()),
             ['name', 'flag', 'group', 'last', 'change_pct'],
             ['change_pct']),
        ]
        for sec_title, df, show_cols, chg_cols_n in sections:
            if df is None or df.empty: continue
            avail = [c for c in show_cols if c in df.columns]
            ws_dash.cell(row, 1).value = sec_title.upper()
            ws_dash.cell(row, 1).font = Font(bold=True, color="00D9A6", size=10)
            row += 1
            for ci, col in enumerate(avail, 1):
                cell = ws_dash.cell(row, ci, col)
                cell.font = s['hdr_font']; cell.fill = s['hdr_fill']
            row += 1
            chg_idx = [i+1 for i, c in enumerate(avail) if c in chg_cols_n]
            for ri, (_, dr) in enumerate(df[avail].iterrows()):
                fill = s['alt_fill'] if ri % 2 == 0 else s['bg_fill']
                for ci, val in enumerate(dr.tolist(), 1):
                    cell = ws_dash.cell(row, ci, val)
                    cell.fill = fill; cell.border = s['thin_border']
                    if ci in chg_idx and isinstance(val, (int, float)):
                        cell.font = s['pos_font'] if val >= 0 else s['neg_font']
                    else:
                        cell.font = s['data_font']
                row += 1
            row += 1
        self._autofit(ws_dash)

        # ── Individual sheets ────────────────────────────────────
        idx = data.get('indices', pd.DataFrame())
        if not (idx is None or (isinstance(idx, pd.DataFrame) and idx.empty)):
            self._sheet_df(wb, "Equity Indices", idx, ts, ['change_pct', 'ytd_pct'])

        bonds = data.get('bonds', pd.DataFrame())
        if not (bonds is None or (isinstance(bonds, pd.DataFrame) and bonds.empty)):
            self._sheet_df(wb, "Fixed Income", bonds, ts, ['chg_bps', 'spread_vs_us'])

        fx = data.get('fx', pd.DataFrame())
        if not (fx is None or (isinstance(fx, pd.DataFrame) and fx.empty)):
            self._sheet_df(wb, "FX", fx, ts, ['change_pct'])

        comm = data.get('commodities', pd.DataFrame())
        if not (comm is None or (isinstance(comm, pd.DataFrame) and comm.empty)):
            self._sheet_df(wb, "Commodities", comm, ts, ['change_pct'])

        vr = data.get('vol_rates', {})
        if vr:
            vr_rows = [{'indicator': v['name'], 'value': v['last'],
                        'chg_pct': v['chg_pct'], 'context': v.get('context', '')}
                       for k, v in vr.items() if isinstance(v, dict) and 'last' in v]
            if vr_rows:
                self._sheet_df(wb, "Vol & Rates", pd.DataFrame(vr_rows), ts, ['chg_pct'])

        crypto = data.get('crypto', pd.DataFrame())
        if not (crypto is None or (isinstance(crypto, pd.DataFrame) and crypto.empty)):
            self._sheet_df(wb, "Crypto", crypto, ts, ['chg_1d', 'chg_7d'])

        cal = data.get('calendar', pd.DataFrame())
        if not (cal is None or (isinstance(cal, pd.DataFrame) and cal.empty)):
            self._sheet_df(wb, "Econ Calendar", cal, ts, [])

        # Save to bytes
        buf = io.BytesIO()
        wb.save(buf)
        buf.seek(0)
        return buf.read()

    def export(self, data: dict, filepath: Optional[str] = None) -> str:
        """Save Excel file to disk. Returns the filepath."""
        if filepath is None:
            filepath = f"/tmp/ravinala_macro_{datetime.now(timezone.utc).strftime('%Y%m%d_%H%M')}.xlsx"
        xl_bytes = self.export_bytes(data)
        with open(filepath, 'wb') as f:
            f.write(xl_bytes)
        return filepath


